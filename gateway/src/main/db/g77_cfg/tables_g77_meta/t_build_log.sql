CREATE TABLE G77_CFG.T_BUILD_LOG (
  BL_ID            NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH 1 CACHE 1000 NOORDER),
  BL_GROUP         VARCHAR2(255),
  BL_MSG_TYPE 	   VARCHAR2(255),
  BL_PROC_NAME 	   VARCHAR2(255),
  BL_TIMESTAMP 	   TIMESTAMP DEFAULT SYSTIMESTAMP,
  BL_MESSAGE 	   VARCHAR2(4000),
  BL_BACKTRACE 	   VARCHAR2(4000),
  BL_TAGNAME 	   VARCHAR2(4000),
  BL_TAG           VARCHAR2(4000),
  BL_TAG1NAME      VARCHAR2(4000),
  BL_TAG1          VARCHAR2(4000),
  BL_TAG2NAME      VARCHAR2(4000),
  BL_TAG2          VARCHAR2(4000),
  BL_TAG3NAME      VARCHAR2(4000),
  BL_TAG3          VARCHAR2(4000),
  BL_TAG4NAME      VARCHAR2(4000),
  BL_TAG4          VARCHAR2(4000),
  BL_CALLSTACK 		 VARCHAR2(4000),
  CONSTRAINT PK_T_BUILD_LOG PRIMARY KEY (BL_ID)
);
