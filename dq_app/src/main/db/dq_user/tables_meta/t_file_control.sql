CREATE TABLE DQ_USER.T_FILE_CONTROL
(
FILE_LOAD_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY (CACHE 1000 NOORDER),
 FILE_ID                       NUMBER(12),
 FILE_NAME                     VARCHAR2(250 BYTE),
 FILE_SOURCE_SYSTEM            VARCHAR2(50 BYTE),
 BATCH_PROCESS_ID              NUMBER(12),
 EXPECTED_RECORDS_PROCESSED    NUMBER(12),
 RECORDS_PROCESSED             NUMBER(12),
 FILE_STATUS                   VARCHAR2(50 BYTE),
 FILE_LOG_MESSAGE              VARCHAR2(1000 BYTE),
 SESSION_ID                    VARCHAR2(250 BYTE),
 USE_CASE_ID                   VARCHAR2(250 BYTE),
 ARRIVAL_TIME                  DATE,
 CONSTRAINT PK_T_FILE_CONTROL PRIMARY KEY (FILE_LOAD_ID) ENABLE
);
