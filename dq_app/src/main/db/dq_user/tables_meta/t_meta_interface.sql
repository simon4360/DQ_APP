CREATE TABLE DQ_USER.T_META_INTERFACE
(
INTERFACE_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH 1 CACHE 1000 NOORDER) 
,  TECHNOLOGY             VARCHAR2(30)         -- aptitude, oracle, etc.
,  APTITUDE_PROJECT       VARCHAR2(100)        -- g77_src_g71, g77_tgt_g77
,  APTITUDE_PROJECT_DESC  VARCHAR2(200)        -- nice description
,  COUNTERPARTY_SYSTEM    VARCHAR2(5)          -- G71, G77, G72...
,  COUNTERPARTY_ROLE      VARCHAR2(10)         -- SOURCE , TARGET
,  STOP_PROJECT_FLAG      VARCHAR2(1) NOT NULL -- compatibility with project config
,  RESET_EVENTS_FLAG      VARCHAR2(1) NOT NULL -- compatibility with project config
,  IS_ACTIVE              VARCHAR2(1) NOT NULL
, CONSTRAINT PK_T_META_INTERFACE PRIMARY KEY (INTERFACE_ID)
);
