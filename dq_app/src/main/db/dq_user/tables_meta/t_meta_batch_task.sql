CREATE TABLE DQ_USER.T_META_BATCH_TASK
(
BATCH_TASK_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH 1 CACHE 1000 NOORDER) 
, INTERFACE_ID         NUMBER         NOT NULL 
, PROCESS_TYPE         VARCHAR2(100)  NOT NULL -- microflow, rule, function, procedure..
, PROCESS_NAME         VARCHAR2(100)  NOT NULL -- microflow name, proc name ...
, PROCESS_DESCRIPTION  VARCHAR2(200)          
, ERROR_THRESHOLD      NUMBER(10)     NOT NULL -- compatibility 
, THRESHOLD_IS_PERCENT VARCHAR2(1)    NOT NULL -- compatibility
, RUN_CONTROL          VARCHAR2(1)    NOT NULL -- Y = upsert a run control record, future functionality 
, ACTIVE_INDICATOR     VARCHAR2(1)    NOT NULL 
, CONSTRAINT PK_T_META_BATCH_TASK PRIMARY KEY (BATCH_TASK_ID)
);
